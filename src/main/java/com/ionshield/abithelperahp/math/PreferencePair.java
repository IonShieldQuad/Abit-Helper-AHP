package com.ionshield.abithelperahp.math;

public class PreferencePair {
    public int a;
    public int b;
    public double value;

    public PreferencePair() {
    }

    public PreferencePair(int a, int b, double value) {
        this.a = a;
        this.b = b;
        this.value = value;
    }
}
